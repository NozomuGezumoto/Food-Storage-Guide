const dict: Record<Lang, Dict> = { en, ja };

type Vars = Record<string, string | number>;

export function getCurrentLanguage(): Lang {
  const locales = getLocales?.() ?? [];
  const first = locales[0];
  const code = first?.languageCode?.toLowerCase() ?? 'en';
  return code === 'ja' ? 'ja' : 'en';
}

export function t(key: string, vars?: Vars): string {
  const lang = getCurrentLanguage();
  const base = dict[lang][key] ?? dict.en[key] ?? key;
  if (!vars) return base;
  return Object.keys(vars).reduce((acc, k) => {
    const value = String(vars[k]);
    return acc.replace(new RegExp(`{${k}}`, 'g'), value);
  }, base);
}

/** Returns the food name in the current UI language. Use for display only. */
export function getFoodDisplayName(food: { id: string; name: string; nameJa?: string }): string {
  const lang = getCurrentLanguage();
  if (lang === 'ja' && food.nameJa) return food.nameJa;
  const key = `foodNames.${food.id}`;
  const translated = t(key);
  return translated === key ? food.name : translated;
}

/** Returns display name for a food by id; use when you only have foodId (e.g. PurchaseItem). */
export function getFoodDisplayNameById(foodId: string, fallbackName: string): string {
  const key = `foodNames.${foodId}`;
  const translated = t(key);
  return translated === key ? fallbackName : translated;
}

